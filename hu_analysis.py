def get_args():
    p = argparse.ArgumentParser(description="HU value distribution & tissue error analysis")
    p.add_argument("--data_root",      type=str, default="/home/dministrator/s2025")
    p.add_argument("--ckpt_base",      type=str, default="checkpoints_임베딩1/stage2_vdm")
    p.add_argument("--vqvae_base",     type=str, default="checkpoints_임베딩1/stage1_vqvae")
    p.add_argument("--output_dir",     type=str, default="eval_results/hu_analysis")
    p.add_argument("--device",         type=int, default=0)
    p.add_argument("--n_sample_steps", type=int, default=200)
    p.add_argument("--batch_size",     type=int, default=4)
    p.add_argument("--num_workers",    type=int, default=4)
    p.add_argument("--val_ratio",      type=float, default=0.2)
    p.add_argument("--seed",           type=int, default=42)
    p.add_argument("--keys", nargs="+", default=None,
                   help="평가할 모델 key 목록 (기본: 전체 8개). "
                        "예: --keys uvit_n1_cpr4 uvit_n5_cpr4")
    return p.parse_args()


def main():
    args   = get_args()
    device = torch.device(f"cuda:{args.device}" if torch.cuda.is_available() else "cpu")
    set_determinism(seed=args.seed)
    out = pathlib.Path(args.output_dir)
    out.mkdir(parents=True, exist_ok=True)

    configs = get_configs(args.keys)
    print(f"분석 대상 모델: {[c['key'] for c in configs]}")

    all_data: dict[str, list] = {}
    for cfg in configs:
        print(f"\n>>> {cfg['key']}")
        try:
            loaded  = load_for_hu(
                cfg, args.data_root, args.ckpt_base, args.vqvae_base,
                device, args.val_ratio, args.seed, args.batch_size, args.num_workers,
            )
            records = collect_hu(cfg, loaded, device, args.n_sample_steps)
            all_data[cfg["key"]] = records
        except Exception as e:
            print(f"  [건너뜀] {e}")

    if not all_data:
        print("분석 결과 없음.")
        return

    # ── 히스토그램 ────────────────────────────────────────────────────────
    plot_hu_histograms(all_data, out)

    # ── anatomy별 오차 통계 ───────────────────────────────────────────────
    anat_df = compute_anatomy_stats(all_data)
    anat_path = out / "hu_error_stats.csv"
    anat_df.to_csv(anat_path, index=False)
    print(f"[저장] {anat_path}")
    print(anat_df.to_string(index=False))

    # ── 조직별 오차 통계 ──────────────────────────────────────────────────
    tissue_df = compute_tissue_stats(all_data)
    tissue_path = out / "hu_tissue_error_stats.csv"
    tissue_df.to_csv(tissue_path, index=False)
    print(f"\n[저장] {tissue_path}")
    print(tissue_df.to_string(index=False))

    print(f"\n완료: {out}/")


if __name__ == "__main__":
    main()
