def compute_anatomy_stats(all_data: dict[str, list]) -> pd.DataFrame:
    rows = []
    for key, records in all_data.items():
        by_anat: dict[str, tuple[list, list]] = {}
        for r in records:
            a = r["anatomy"]
            if a not in by_anat:
                by_anat[a] = ([], [])
            by_anat[a][0].append(r["pred_hu"])
            by_anat[a][1].append(r["gt_hu"])
        for anat, (preds, gts) in by_anat.items():
            rows.append({"model": key, "anatomy": anat,
                         **hu_errors(np.concatenate(preds), np.concatenate(gts))})
    return pd.DataFrame(rows)


def compute_tissue_stats(all_data: dict[str, list]) -> pd.DataFrame:
    rows = []
    for key, records in all_data.items():
        pred_all = np.concatenate([r["pred_hu"] for r in records])
        gt_all   = np.concatenate([r["gt_hu"]   for r in records])
        for tissue, (lo, hi) in TISSUES.items():
            mask = tissue_mask(gt_all, lo, hi)
            if mask.sum() == 0:
                continue
            rows.append({
                "model"   : key,
                "tissue"  : tissue,
                "n_pixels": int(mask.sum()),
                **hu_errors(pred_all[mask], gt_all[mask]),
            })
    return pd.DataFrame(rows)

