# ---------------------------------------------------------------------------
# Config helpers
# ---------------------------------------------------------------------------

def get_configs(keys: list[str] | None) -> list[dict]:
    if not keys:
        return MODEL_CONFIGS
    return [c for c in MODEL_CONFIGS if c["key"] in keys]

# ---------------------------------------------------------------------------
# Gen-file-based HU collection (모델 로딩 없이 .mha에서 바로 HU 배열 수집)
# ---------------------------------------------------------------------------

def collect_hu_from_gen(
    cfg: dict,
    gen_dir: pathlib.Path,
    data_root: str,
    val_ratio: float = 0.2,
    seed: int = 42,
    batch_size: int = 4,
    num_workers: int = 4,
) -> list[dict]:
    """gen_dir/{key}/{subj_id}.mha + dataset GT → HU 배열 records 반환."""
    key       = cfg["key"]
    n         = cfg["n"]
    mid       = n // 2
    model_dir = gen_dir / key

    with open(model_dir / "meta.json") as f:
        subj_anatomy: dict[str, str] = json.load(f)

    val_loader = build_val_loader(
        data_root, n, val_ratio=val_ratio, seed=seed,
        batch_size=batch_size, num_workers=num_workers,
    )

    records: list[dict] = []
    for batch in tqdm(val_loader, desc=key, leave=False):
        gt_norm  = batch["ct"][:, mid:mid+1].float()   # (B, 1, H, W) [0,1]
        subj_ids = batch["subj_id"]

        for i, sid in enumerate(subj_ids):
            gen_arr = load_mha(model_dir / f"{sid}.mha")   # (H, W) [0,1]
            pred_hu = to_hu(gen_arr).ravel()
            gt_hu   = to_hu(gt_norm[i, 0].numpy()).ravel()
            records.append({
                "anatomy" : subj_anatomy.get(sid, "UNKNOWN"),
                "pred_hu" : pred_hu,
                "gt_hu"   : gt_hu,
            })

    return records

# ---------------------------------------------------------------------------
# Inference-based HU collection (모델 로드 후 직접 샘플링)
# ---------------------------------------------------------------------------

@torch.no_grad()
def collect_hu(cfg: dict, loaded: dict, device: torch.device,
               n_sample_steps: int = 200) -> list[dict]:
    from train.stage2_vdm import _prepare_cond, sample_conditional

    ct_ae        = loaded["ct_ae"]
    cbct_ae      = loaded["cbct_ae"]
    vdm          = loaded["vdm"]
    scale_factor = loaded["scale_factor"]
    val_loader   = loaded["val_loader"]
    subj_anatomy = loaded["subj_anatomy"]
    backbone     = cfg["backbone"]      # load_model_for_eval에 없으므로 cfg에서
    n            = cfg["n"]
    mid          = n // 2

    records: list[dict] = []
    for batch in tqdm(val_loader, desc=cfg["key"], leave=False):
        ct_img   = batch["ct"].to(device)
        cbct_img = batch["cbct"].to(device)
        subj_ids = batch["subj_id"]

        z_cond    = cbct_ae.encode_stage_2_inputs(cbct_img) * scale_factor
        cond      = _prepare_cond(z_cond, backbone)
        sampled_z = sample_conditional(vdm, cond, n_sample_steps, device)
        ct_gen    = ct_ae.decode_stage_2_outputs(sampled_z / scale_factor).float()
        ct_gt     = ct_img[:, mid:mid+1].float()

        pred_hu = to_hu(ct_gen.cpu().numpy())   # (B, 1, H, W)
        gt_hu   = to_hu(ct_gt.cpu().numpy())

        for i, sid in enumerate(subj_ids):
            records.append({
                "anatomy" : subj_anatomy.get(sid, "UNKNOWN"),
                "pred_hu" : pred_hu[i].ravel(),
                "gt_hu"   : gt_hu[i].ravel(),
            })

    return records

# ---------------------------------------------------------------------------
# Plot: HU histogram overlay
# ---------------------------------------------------------------------------

def plot_hu_histograms(all_data: dict[str, list], output_dir: pathlib.Path):
    models   = list(all_data.keys())
    bins     = np.linspace(HU_MIN, HU_MAX, 200)
    fig, axes = plt.subplots(1, len(models), figsize=(5 * len(models), 4), sharey=False)
    if len(models) == 1:
        axes = [axes]

    for ax, key in zip(axes, models):
        records  = all_data[key]
        pred_all = np.concatenate([r["pred_hu"] for r in records])
        gt_all   = np.concatenate([r["gt_hu"]   for r in records])
        ax.hist(gt_all,   bins=bins, density=True, alpha=0.5, color="#2196F3", label="GT CT")
        ax.hist(pred_all, bins=bins, density=True, alpha=0.5, color="#F44336", label="Gen CT")
        ax.set_title(key, fontsize=9)
        ax.set_xlabel("HU value")
        ax.set_ylabel("Density")
        ax.legend(fontsize=7)

    fig.suptitle("HU Value Distribution: Generated vs Ground-truth CT", fontsize=11)
    fig.tight_layout()
    path = output_dir / "hu_histogram.png"
    fig.savefig(path, dpi=150)
    plt.close(fig)
    print(f"[저장] {path}")

